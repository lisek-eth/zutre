<template>
  <li :class="itemClass"><slot></slot></li>
</template>

<script>
/**
 * NavItem
 * 
 * @author Maciej Lisowski <maciej.lisowski.elk@gmail.com>
 */
export default {
  name: 'NavItem',
  props: {
    active: {
      type: Boolean
    }
  },
  computed: {
    itemClass: function() {
      let css = {
        'nav-item': true,
        active: (this.active === true) ? true : false
      }

      return css
    }
  }
}
</script>
